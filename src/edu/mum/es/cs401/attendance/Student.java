package edu.mum.es.cs401.attendance;

public class Student {
	String ID;
	String firtsName;
	String lastName;
	String Barcode;
	
	public Student(String iD, String firtsName, String lastName, String barcode) {
		super();
		ID = iD;
		this.firtsName = firtsName;
		this.lastName = lastName;
		Barcode = barcode;
	}
	
	
}
